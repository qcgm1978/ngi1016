/**
 * @param ArrGps gps log字符串或对象构成的数组
 * @return {Array} 根据返回的数据就可进行相应gps log模拟导航
 */
    function getGPSPoint(ArrGps){
        var gpsArr = [] ;
        if (ArrGps[0] instanceof Object) {
            gpsArr = ArrGps;
        }
        else if (typeof ArrGps[0] == 'string') {
            var second = 0 ;
            for(var i = 0 ; i < ArrGps.length ; i++) {
                if (typeof ArrGps[i] == 'string') {
                    var arrLine = ArrGps[i].split(" ");
                    var arrN = [];
                    for(var j = 0; j < arrLine.length; j++) {
                        arrN.push(parseFloat(arrLine[j]));
                    }
                    var gpsinfo = {};
                    gpsinfo.longitude         = arrN[0];
                    gpsinfo.latitude         = arrN[1];
                    gpsinfo.speed       = arrN[2];
                    gpsinfo.direction       = arrN[3];
                    var date  = new Date();
                    gpsinfo.year      = date.getFullYear();
                    gpsinfo.month     = date.getMonth();
                    gpsinfo.day       = date.getDay();
                    gpsinfo.hour      = date.getHours();
                    gpsinfo.minute    = date.getMinutes();
                    gpsinfo.second    = ++second % 60;//date.getSeconds();
                    gpsArr.push(gpsinfo) ;
                }
            }
        }
        return gpsArr ;
    }

